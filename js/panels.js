// GAMES

// SCARLET RADIANCE
function openScarlet() {
	document.getElementById("scarlet").style.visibility = "visible";
	document.getElementById("scarlet").style.opacity = "1";
    document.getElementById("scarlet").style.transitionDelay = "0s";
}

function closeScarlet() {
	document.getElementById("scarlet").style.visibility = "hidden";
	document.getElementById("scarlet").style.opacity = "0";
    document.getElementById("scarlet").style.transition = "visibility 0s ease-in-out 0.5s, opacity 0.5s ease-in-out";
}


